#include "monty.h"

/**
 * _queue - a function that prints the top
 * @head: head of the stack
 * @count: number of lines
*/
void _queue(stack_t **head, unsigned int count)
{
	(void)head;
	(void)count;
	alx.sc = 1;
}

/**
 * _stack - prints the top
 * @head: stack head
 * @count: number of lines
*/
void _stack(stack_t **head, unsigned int count)
{
	(void)head;
	(void)count;
	alx.sc = 0;
}
/**
  *_nop- empty function
  *@head: stack head
  *@count: number of lines
 */
void _nop(stack_t **head, unsigned int count)
{
	(void) count;
	(void) head;
}
